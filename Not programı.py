note = float(input("Notunuzu giriniz:"))
if (note >= 90):
    print("AA")
elif(90>note>=80):
    print("BA")
elif(80>note>=70):
    print("BB")
elif(70>note>=60):
    print("CB")
elif(60>note>=50):
    print("CC")
elif(50>note>=40):
    print("DC")
elif(40>note>35):
    print("DD")
else:
    print("Notunuz : Kaldınız")